package dev.mandevilla.Questoes.Q7.Classes;

public class Boi {
    private final int id;
    private final float peso;
    
    public Boi(int id, float peso) {
        this.id = id;
        this.peso = peso;
    }

    public int getId() {
        return id;
    }

    public float getPeso() {
        return peso;
    }
}
