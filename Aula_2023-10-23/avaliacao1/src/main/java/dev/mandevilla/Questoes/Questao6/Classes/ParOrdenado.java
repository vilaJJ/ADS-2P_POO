package dev.mandevilla.Questoes.Questao6.Classes;

public class ParOrdenado {
    private final double x;
    private final double y;

    public ParOrdenado(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double obterX() {
        return x;
    }
    public double obterY() {
        return y;
    }
}
